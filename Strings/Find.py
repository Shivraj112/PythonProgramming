# Finding a substring is like using the "find" option 
# You can check if a specific text is exists in a string using the .find()

text = "Learning Python is fun!"
print(text.find("Python"))  # The text which you want to extract is quoted in double " "quotation 
print(text.find("fun")) 

shiva = "Aum Namaha Shivaya , Shiva Shiva"
print(shiva.find("Aum"))
print(shiva.find("Shiva"))